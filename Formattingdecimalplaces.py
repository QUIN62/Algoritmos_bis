def round_to_two_decimal_places(num):
    # Redondear el n√∫mero a dos decimales
    return round(num, 2)

# Ejemplo de uso
print(round_to_two_decimal_places(5.5589))  # Salida: 5.56
print(round_to_two_decimal_places(3.3424))  # Salida: 3.34
print(round_to_two_decimal_places(2.0))      # Salida: 2.0
print(round_to_two_decimal_places(1.999))    # Salida: 2.0
